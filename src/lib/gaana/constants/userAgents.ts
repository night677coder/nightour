/**
 * Selects a random user agent string from the available list.
 *
 * @returns {string} Random user agent string
 *
 * @example
 * ```typescript
 * const ua = getRandomUserAgent()
 * // Returns: "Mozilla/5.0 (Windows NT 10.0; Win64; x64) ..."
 * ```
 */
export function getRandomUserAgent(): string {
  const randomIndex = Math.floor(Math.random() * userAgents.length)
  return userAgents[randomIndex]
}

/**
 * Generates a complete set of browser-like HTTP headers for API requests.
 *
 * Creates realistic headers including User-Agent, Accept, Origin, Referer, and
 * browser-specific headers (Sec-CH-UA for Chrome, etc.) to make requests
 * appear legitimate.
 *
 * @param {string} [userAgent] - Optional user agent string. If not provided, a random one is selected.
 * @returns {Record<string, string>} Object containing HTTP headers
 *
 * @example
 * ```typescript
 * const headers = getBrowserHeaders()
 * // Returns: {
 * //   'User-Agent': 'Mozilla/5.0 ...',
 * //   'Accept': 'application/json, text/plain, *'
 * //   'Origin': 'https://gaana.com',
 * //   ...
 * // }
 * ```
 */
export function getBrowserHeaders(userAgent?: string): Record<string, string> {
  const ua = userAgent || getRandomUserAgent()
  const isMobile = ua.includes('Mobile') || ua.includes('iPhone') || ua.includes('Android')
  const isChrome = ua.includes('Chrome')
  const isFirefox = ua.includes('Firefox')
  const isSafari = ua.includes('Safari') && !ua.includes('Chrome')

  const headers: Record<string, string> = {
    'User-Agent': ua,
    Accept: 'application/json, text/plain, */*',
    'Accept-Language': 'en-US,en;q=0.9',
    'Accept-Encoding': 'gzip, deflate, br',
    Origin: 'https://gaana.com',
    Referer: 'https://gaana.com/',
    Connection: 'keep-alive',
    'Sec-Fetch-Dest': 'empty',
    'Sec-Fetch-Mode': 'cors',
    'Sec-Fetch-Site': 'same-origin'
  }

  // Add Chrome-specific headers
  if (isChrome) {
    headers['sec-ch-ua'] = '"Chromium";v="136", "Google Chrome";v="136", "Not.A/Brand";v="99"'
    headers['sec-ch-ua-mobile'] = isMobile ? '?1' : '?0'
    headers['sec-ch-ua-platform'] = isMobile ? '"Android"' : '"Windows"'
  }

  // Add host header
  headers['Host'] = 'gaana.com'

  return headers
}


